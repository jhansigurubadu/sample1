public class JavaOperators {
    public static void main(String[] args) {
        int num1=10;
        int num2=30;
        int sum=num1+num2;
        System.out.println("sum=+"+sum);
        int sum2=num1-num2;
        System.out.println("sum2"+sum2);
        int sum3=num1*num2;
        System.out.println("sum3"+sum3);
        int sum4=num1%num2;
        System.out.println("sum4"+sum4);
        int sum5=num1/num2;
        System.out.println("sum5"+sum5);

    }



}
