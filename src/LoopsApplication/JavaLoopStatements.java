package LoopsApplication;

public class JavaLoopStatements {
    public static void main(String[] args) {
        int sum= 10;
        int b=2;
        while(b>9);{
            System.out.println("codexbox");
            b++;
            int c;
            do {
                c = 2;
                System.out.println(c);
                c--;
            }
            while(c<0);
        }
        for(int i=1;i<=5;i++){
          sum=sum+i;
            System.out.println("sum:"+sum);
        }
    }
}
